package Model;

public class Produto {
	int    Row_ID;
	String Descricao;
	float Preco;
	
	public int getRow_ID() {
		return Row_ID;
	}
	public void setRow_ID(int row_ID) {
		Row_ID = row_ID;
	}
	public String getDescricao() {
		return Descricao;
	}
	public void setDescricao(String descricao) {
		Descricao = descricao;
	}
	public float getPreco() {
		return Preco;
	}
	public void setPreco(float preco) {
		Preco = preco;
	}
}
